class Solution {
    public int numIslands(char[][] grid) {
        int m = grid.length;
        if (m == 0) {
            return 0;
        }
        int n = grid[0].length;
        int ans = 0;
        for (int i = 0; i < m; i++) {
            for (int j = 0; j < n; j++) {
                if (grid[i][j] == '1') {
                    ans++;
                    traverse(grid, i, j, m, n);
                }
            }
        }
        return ans;
    }

    private void traverse(char[][] grid, int i, int j, int m, int n) {
        if (i < 0 || i >= m || j < 0 || j >= n || grid[i][j] != '1') {
            return;
        }

        grid[i][j] = '0';

        traverse(grid, i+1, j, m, n);
        traverse(grid, i-1, j, m, n);
        traverse(grid, i, j+1, m, n);
        traverse(grid, i, j-1, m, n);
    }
}
